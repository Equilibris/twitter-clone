const randomElement = <T>(l: T[]): T => l[Math.trunc(Math.random() * l.length)]
const randomLArray = (a: number, b: number): number[] => [
	...Array(Math.trunc(Math.random() * (b - a) + a)).keys(),
]

const word = () => randomElement(lorem)

const sentence = () => randomLArray(5, 15).map((_) => word())

const formattedSentence = () => {
	const s = sentence()
	s[0] = s[0].replace(/^./, (x) => x.toUpperCase())

	const punctuation = [...'.'.repeat(10), ...'?'.repeat(4), ...'!'.repeat(3)]

	return s.join(' ') + randomElement(punctuation)
}

export const text = () => {
	return randomLArray(1, 3)
		.map((_) => formattedSentence())
		.join(' ')
}
